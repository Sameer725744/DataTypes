
//Write a Java program to print an int, a double and a char on screen.

public class Ques_1 {
    public static void main(String[] args) {
        // Declare and initialize an int variable
        int num = 10;

        // Declare and initialize a double variable
        double pi = 3.14;

        // Declare and initialize a char variable
        char letter = 'A';

        // Print the values of the variables on screen
        System.out.println("The int value is :" + num);
        System.out.println("The double value is :" + pi);
        System.out.println("The char value is :" + letter);
    }
}
